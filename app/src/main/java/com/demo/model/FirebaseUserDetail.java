public class FirebaseUserDetail {
    String userId;
    private String userName;
    private String userAddress;
    private String userEmail;
    private String userPinCode;
    private String userAdharId;
    private String userMobileNumber;
    private String userGender;
    private String userDOB;
    private String userMedicalHistory;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserAddress() {
        return userAddress;
    }

    public void setUserAddress(String userAddress) {
        this.userAddress = userAddress;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserPinCode() {
        return userPinCode;
    }

    public void setUserPinCode(String userPinCode) {
        this.userPinCode = userPinCode;
    }

    public String getUserAdharId() {
        return userAdharId;
    }

    public void setUserAdharId(String userAdharId) {
        this.userAdharId = userAdharId;
    }

    public String getUserMobileNumber() {
        return userMobileNumber;
    }

    public void setUserMobileNumber(String userMobileNumber) {
        this.userMobileNumber = userMobileNumber;
    }

    public String getUserGender() {
        return userGender;
    }

    public void setUserGender(String userGender) {
        this.userGender = userGender;
    }

    public String getUserDOB() {
        return userDOB;
    }

    public void setUserDOB(String userDOB) {
        this.userDOB = userDOB;
    }

    public String getUserMedicalHistory() {
        return userMedicalHistory;
    }

    public void setUserMedicalHistory(String userMedicalHistory) {
        this.userMedicalHistory = userMedicalHistory;
    }
}
